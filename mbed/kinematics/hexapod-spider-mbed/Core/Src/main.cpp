/* USER CODE BEGIN 0 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart -> Instance == UART4)
    {
        HAL_UART_Receive_IT(&huart4, &rData, 1);

        receiveBuffer[pos++] = rData;

        if(pos == 1 && receiveBuffer[0] != ':'){
			memset(receiveBuffer, 0, 8);
			pos = 0;
        }

        if (rData == '\n')
		{
        	Message_t msg = Message_Parser((char*)receiveBuffer);
			memset(receiveBuffer, 0, 8);
			pos = 0;

        	if(msg.accuracy == true)
        	{
        		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_3, GPIO_PIN_SET);
        		cmdFlag = true;

        		switch (msg.type)
        		{
        			case Cmd_Go_Flash:

        			   flashFlag = true;

        			break;

					case Cmd_Go_Forw:
	    				dir = Direction_Forward;
	    				walkFlag = true;
						break;
					case Cmd_Go_Left:
						dir = Direction_Left;
						walkFlag = true;
						break;
					case Cmd_Go_Rote:
						rotateFlag = true;
						break;
					case Cmd_Go_Rigt:
						dir = Direction_Right;
						walkFlag = true;
						break;
					case Cmd_Go_Back:
						dir = Direction_Back;
						walkFlag = true;
						break;
					case Cmd_Dance:
						dancingFlag = true;
						break;
					case Cmd_Balance:

						if(balanceFlag == false){
							balanceFlag = true;
						}
						else{
							balanceFlag = false;
						}

						break;
					case Cmd_Go_Stop:
						walkFlag = false;
						rotateFlag = false;
						dancingFlag = false;
						break;

					case Cmd_Step_Tx:
						spider.MoveHexapodBodyXYZ(msg.data, 0, 0);
						break;
					case Cmd_Step_Ty:
						spider.MoveHexapodBodyXYZ(0, msg.data, 0);
						break;
					case Cmd_Step_Tz:
						spider.MoveHexapodBodyXYZ(0, 0, msg.data);
						break;

					case Cmd_Step_Rx:
						spider.RotateHexapodBodyXYZ(msg.data, 0, 0);
						break;
					case Cmd_Step_Ry:
						spider.RotateHexapodBodyXYZ(0, msg.data, 0);
						break;
					case Cmd_Step_Rz:
						spider.RotateHexapodBodyXYZ(0, 0, msg.data);
						break;

					case Cmd_Reset:

						  NVIC_SystemReset();

						  while (1)
						  {

						  }

						break;

					default:
						break;
				}

        	}

		}
    }
}
